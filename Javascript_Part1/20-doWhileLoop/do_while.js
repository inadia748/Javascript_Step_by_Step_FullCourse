

console.log('While Loop');
let i = 10;
while (i <= 5) {
    console.log(i);
    i++;
}
console.log('It will not print anything since the condition does not match');



// do while loop
console.log('do while loop');
let t = 10;
do {
    console.log(t);
    t++;
} while(t <= 5);

console.log('In do-while loop, the condition is checked at last. It is not checked in the beginning');




